const months = param => {
  if (param === "01") {
    return "Jan";
  }
  if (param === "02") {
    return "Feb";
  }
  if (param === "03") {
    return "Mar";
  }
  if (param === "04") {
    return "Apr";
  }
  if (param === "05") {
    return "May";
  }
  if (param === "06") {
    return "Jun";
  }
  if (param === "07") {
    return "Jul";
  }
  if (param === "08") {
    return "Aug";
  }
  if (param === "09") {
    return "Sep";
  }
  if (param === "10") {
    return "Oct";
  }
  if (param === "11") {
    return "Nov";
  }
  if (param === "12") {
    return "Dec";
  }
};

export { months };
